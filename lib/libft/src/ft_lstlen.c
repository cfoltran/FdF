#include "libft.h"

size_t	ft_lstlen(t_list *lst)
{
	size_t	i;
	t_list	*curr;

	i = 0;
	curr = lst;
	while (curr)
	{
		curr = curr->next;
		i++;
	}
	return (i);
}
