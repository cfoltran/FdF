#include "libft.h"

char	*ft_strrev(char *str)
{
	char	*s1;
	char	*s2;

	s1 = str;
	s2 = str + ft_strlen(str) - 1;
	if (!str || !(*str))
		return (str);
	while (--s2 > ++s1)
	{
		*s1 ^= *s2;
		*s2 ^= *s1;
		*s1 ^= *s2;
	}
	return (str);
}
